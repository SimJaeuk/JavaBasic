public class TernaryExam3 {
    // 삼항연산자를 사용하여 (if사용 금지)
    // 1 이면 1st 2이면 2nd 3이면 3rd 4이면 4th
    // 를 출력하는 프로그램을 작성하시오.
    public static void main(String[] args) {
    int a = 1;
    
    
    System.out.println((a == 1) ? "1st" : (a == 2) ? "2nd" : (a == 3) ? "3rd" : (a == 4) ? "4th" : "?");
    
    

 }
}